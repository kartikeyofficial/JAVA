public class Rectangle1 {
    private int length;
    private int width;
    public  void setLength(int len){
        length=len;
    }
    public int getLength(){
        return length;
    }
    public void setWidth(int wid){
        width=wid;
    }
    public int getWidth(){

        return width;
    }
    int area(){
        return length*width;
    }
    
}
