public class IncreamentDecrement {
    public static void main(String[] args) {
        float a = 65;
        
        System.out.println(a++);
        System.out.println(a);
        System.out.println(++a);
        System.out.println(a);


        System.out.println(a--);
        System.out.println(a);
        System.out.println(--a);
        System.out.println(a);

        int y=7;
        int x= ++y *8;
        System.out.println(x);


        char ch='a';
        System.out.println(++ch);
        System.out.println(ch++);
        System.out.println(ch);
    }
}
