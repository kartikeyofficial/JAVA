
    public class AssignmentOperator {
        public AssignmentOperator() {
        }
     
        public static void main(String[] args) {
           int a = 10;
           System.out.println(10);
           a += 5;
           System.out.println(a);
           a -= 3;
           System.out.println(a);
           System.out.println(a *= 2);
           System.out.println(a /= 2);
           System.out.println(a %= 2);
        }
     }
     

